    """
    DocExtension - Enum class for parser file extensions.
    """
    docx = 'docx'
    doc = 'doc'
    pdf = 'pdf'
    xls = 'xls'
    xlsx = 'xlsx'
    odt = 'odt'
    rtf = 'rtf'
    md = 'md'
    zip = 'zip'
    seven_z = '7z'
    txt = 'txt'